package SturBack;

public class StarBucks {
    public String kofe;
    public String kofeninNovu;
    public String olchu;

    public StarBucks(String kofe) {
        this.kofe = kofe;
    }
    public StarBucks(String kofe, String kofeninNovu) {
        this.kofe = kofe;
        this.kofeninNovu = kofeninNovu;
    }
    public StarBucks(String kofe, String kofeninNovu, String olchu) {
        this.kofe = kofe;
        this.kofeninNovu = kofeninNovu;
        this.olchu = olchu;
    }
}
